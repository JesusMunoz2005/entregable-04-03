package u6u7.entregable;

public class Manager {

private String nombre;
private int edad;
private int telef;

public Manager(String nombre, int edad, int telef){
	this.nombre=nombre;
	this.edad=edad;
	this.telef=telef;
}

public String getNombre() {
	return nombre;
}

public int getEdad() {
	return edad;
}

public int getTelef() {
	return telef;
}
	

}
